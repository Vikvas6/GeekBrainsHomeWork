#'''
from random import randint
path = 'somefilewithnumbers.txt'

with open(path, 'a', encoding='utf-8') as f:
    for _ in range(2500):
        f.write(str(randint(0,9)))

max_cnt = 0
max_char = ''
lst_char = '-1'
cnt = 1
with open(path, 'r', encoding='utf-8') as f:
    for line in f:
        for i in line:
            if i == lst_char:
                cnt += 1
            else:
                if cnt > max_cnt:
                    max_cnt = cnt
                    max_char = lst_char
                cnt = 1
                lst_char = i
print(max_char * max_cnt)
